double lookup(unsigned int index){
  index = index % (TABLE_SIZE*4);
  if(index < TABLE_SIZE){
    return sinTable[index];
  }else if(index < (TABLE_SIZE*2)){
    return sinTable[(TABLE_SIZE*2)-index];
  }else if (index < (TABLE_SIZE*3)){
    return -sinTable[index - TABLE_SIZE*2];
  }else{
    return -sinTable[(4*TABLE_SIZE)-index];
  }
}

double mysin(double radians){
  if(radians <0){
    unsigned int index = -(radians*TABLE_SIZE)/M_PI_2;
    return -lookup(index);
  }
  unsigned int index = (radians*TABLE_SIZE)/M_PI_2;
  return lookup(index);
}

